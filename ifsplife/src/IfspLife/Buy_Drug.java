    private void JTextField_Codigo_RemedioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTextField_Codigo_RemedioKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String sql = "select * from purchase where BARCODE='"+JTextField_Codigo_Remedio.getText()+"' " ;
            try{
                pre=con.prepareStatement(sql);
                res=pre.executeQuery();
                if(res.next()){
                    String Name = res.getString("NAME");
                    JTextField_Nome_Remedio.setText(Name);

                    String Type = res.getString("TYPE");
                    JComboBox_Tipo_Remedio.setSelectedItem(Type);

                    String C_name = res.getString("COMPANY_NAME");
                    JComboBox_Nome_Empresa.setSelectedItem(C_name);

                    String Price = res.getString("PRICE");
                    JTextField_PrecoCusto_Remedio.setText(Price);

                    String Quantity = res.getString("QUANTITY");
                    JComboBox_Quantidade_Remedio.setSelectedItem(Quantity);

                    String Amount = res.getString("AMOUNT");
                    JTextFieldPreco.setText(Amount);
                }else {
                    JOptionPane.showMessageDialog(null,"This Drug did not Exist before","Error",2);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e.getMessage(),"Error",2);
            }
        }
    }//GEN-LAST:event_JTextField_Codigo_RemedioKeyPressed

